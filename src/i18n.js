let current = "en";
const stored = localStorage.getItem("hn-lang");
if (STRINGS[stored]) {
  current = stored;
} else if (navigator.language) {
  const nav = navigator.language.slice(0, 2).toLowerCase();
  if (STRINGS[nav]) {
    current = nav;
  }
}

export function getLang() {
  return current;
}

export function setLang(lang) {
  if (!STRINGS[lang]) {
    return;
  }
  localStorage.setItem("hn-lang", lang);
  current = lang;
}

export function t(key, params) {
  let text = STRINGS[current][key];
  if (text === undefined) {
    text = STRINGS.en[key];
  }
  if (text === undefined) {
    text = key;
  }
  if (params) {
    for (const name of Object.keys(params)) {
      text = text.split("{" + name + "}").join(String(params[name]));
    }
  }
  return text;
}

export function applyStatic() {
  const nodes = document.querySelectorAll("[data-i18n]");
  for (const node of nodes) {
    node.textContent = t(node.getAttribute("data-i18n"));
  }
  const htmlNodes = document.querySelectorAll("[data-i18n-html]");
  for (const node of htmlNodes) {
    node.innerHTML = t(node.getAttribute("data-i18n-html"));
  }
  const phNodes = document.querySelectorAll("[data-i18n-ph]");
  for (const node of phNodes) {
    node.setAttribute("placeholder", t(node.getAttribute("data-i18n-ph")));
  }
  document.documentElement.lang = current;
}

export async function autoTranslate(text, sourceLang = "fr") {
  if (current === sourceLang) return text;
  try {
    const url = "https://translate.googleapis.com/translate_a/single?client=gtx&sl=" + sourceLang + "&tl=" + current + "&dt=t&q=" + encodeURIComponent(text);
    const res = await fetch(url);
    const data = await res.json();
    if (data && data[0] && data[0][0] && data[0][0][0]) {
      return data[0][0][0];
    }
    return text;
  } catch (err) {
    return text;
  }
}
